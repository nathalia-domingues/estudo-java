package com.projeto.variaveisjava;

public class Variavel {

	public static void main(String[] args) {
		

		byte variavelByte = 10;
		int variavelInt = 10000;
		long variavelLong = 1000000;
		float variavelFloat = 1.2f;
		double variavelDouble = 1.232323;
		boolean variavelBoolean = true;
		String variavelString = "Nathália";
		
		System.out.println(variavelByte);
		System.out.println(variavelInt);
		System.out.println(variavelLong);
		System.out.println(variavelFloat);
		System.out.println(variavelDouble);
		System.out.println(variavelBoolean);
		System.out.println(variavelString + variavelByte);
	}

}
